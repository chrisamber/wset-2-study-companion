// Derived: a reverse index from region (GI) -> varieties grown there.
export interface RegionEntry {
  gi: string;
  country: string;
  varieties: Variety[];
}

export function regionIndex(): RegionEntry[] {
  const map = new Map<string, RegionEntry>();
  for (const v of VARIETIES) {
    for (const r of v.regions) {
      const key = `${r.gi}|${r.country}`;
      if (!map.has(key)) map.set(key, { gi: r.gi, country: r.country, varieties: [] });
      map.get(key)!.varieties.push(v);
    }
  }
  return [...map.values()].sort((a, b) =>
    a.country === b.country ? a.gi.localeCompare(b.gi) : a.country.localeCompare(b.country)
  );
}

export function varietyById(id: string): Variety | undefined {
  return VARIETIES.find((v) => v.id === id);
}

export const CLIMATE_VARIETIES = VARIETIES.filter((v) => v.climate);
